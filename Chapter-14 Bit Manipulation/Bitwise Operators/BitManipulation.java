public class BitManipulation{
  public static void main(String args[]){
    //Binary AND
    System.out.println((5 & 6));

    //Binary OR 
    System.out.println((5 | 6));

    //Binary XOR 
    System.out.println((5 ^ 6));

    //Binary 1's Complement 
    System.out.println((~5));
    System.out.println((~0));  // -1
  
    //Binary Left Shift
    System.out.println((5<<2));

    //Binary right Shift
    System.out.println((6>>1));
  }
}
