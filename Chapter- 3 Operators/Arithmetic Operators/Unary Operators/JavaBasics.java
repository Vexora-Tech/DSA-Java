import java.util.*;

public class JavaBasics{
  public static void main(String args[]){
    //pre increment
    int a = 10;
    int b = ++a;            
    System.out.println(a);
    System.out.println(b);

    //post increment
    int a = 10;
    int b = a++;            
    System.out.println(a);
    System.out.println(b);


    //pre decrement
    int a = 10;
    int b = --a;            
    System.out.println(a);
    System.out.println(b);

    //post decrement
    int a = 10;
    int b = a--;            
    System.out.println(a);
    System.out.println(b);
  } 
}
