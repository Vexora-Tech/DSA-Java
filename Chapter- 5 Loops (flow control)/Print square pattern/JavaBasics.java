import java.util.*;

public class JavaBasics{
  public static void main(String args[]){
    //from for Loop
    for(int i=1; i<=4; i++){
      System.out.println("* * * *");
    }

    //from while Loop
    int i = 1;
    while(i<=4){
      System.out.println("* * * *");
      i++;
    }
  } 
}
